package Class.Areas;

/**
 *
 * @author ariel
 */
public class Triangulo extends Area{
    private Double base;
    private Double altura;

    public Double getBase() {
        return base;
    }

    public void setBase(Double base) {
        this.base = base;
    }

    public Double getAltura() {
        return altura;
    }

    public void setAltura(Double altura) {
        this.altura = altura;
    }

    @Override
    public Double Area() {
        double area;
        area = (this.getBase()*getAltura())/2;
        this.setArea(area);
        
        return this.getArea();
    }
    
    
}
